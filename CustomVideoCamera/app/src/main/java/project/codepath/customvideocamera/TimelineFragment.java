    private static final String TAG = "TimelineFragment";
    AutoFitTextureView mTextureView;
    Button mButtonVideo;

    List<QuestionCard> mCards;
    RecyclerView recyclerView;
    GaffeRecyclerAdapter adapter;

    public static TimelineFragment newInstance() {
        return new TimelineFragment();
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);


        mCards = new ArrayList<>();

        for(int i = 0; i < 10; i++) {
            mCards.add(new QuestionCard("", "Trump or Drumpf?"));
            Log.i("INFO", mCards.get(i).title);
        }

        adapter = new GaffeRecyclerAdapter(mCards);

        adapter.setRecordActionListener(new RecordActionListener() {
            @Override
            public void onRecordButtonClick(View view, int position) {
                mTextureView = (AutoFitTextureView) view.findViewById(R.id.texture);
                mButtonVideo = (Button)view.findViewById(R.id.openCamera);
                StartCameraPreview();
                Toast.makeText(getActivity(), "position " + position, Toast.LENGTH_SHORT).show();

                mButtonVideo.setOnClickListener(new View.OnClickListener() {
                    @Override
                    public void onClick(View v) {
                        if(mIsRecordingVideo) {
                            stopRecordingVideo();
                        }else{
                            startRecordingVideo();
                        }
                    }
                });
            }
        });

    }

